package model;

public class Student {

    String name;
    String no;
    String sex;

    public String getName() {
        return name;
    }

    public String getNo() {
        return no;
    }

    public String getSex() {
        return sex;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

}
